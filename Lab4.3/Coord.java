//Coord
//Allows creation and modification of coordinates

public class Coord {

	private double x;
	private double y;

	public Coord(double xPos, double yPos) {
		x = xPos;
		y = yPos;
	}

	public void setX(double newX) {
		x = newX;
	}
	
	public void setY(double newY) {
		y = newY;
	}

	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
}//This is a useless test comment please INGORE!
